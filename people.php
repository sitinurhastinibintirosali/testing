        <p align="justify"><strong>        People: </strong></p>
        <p>	Cultures have been meeting and mixing in Malaysia since the very beginning of its history. More than fifteen hundred years ago a Malay kingdom in Bujang Valley welcomed traders from China and India. With the arrival of gold and silks, Buddhism and Hinduism also came to Malaysia.</p> 
        <p>A thousand years later, Arab traders arrived in Malacca and brought with them the principles and practices of Islam. By the time the Portuguese arrived in Malaysia, the empire that they encountered was more cosmopolitan than their own.</p> 
<p>Malaysia's cultural mosaic is marked by many different cultures, but several in particular have had especially lasting influence on the country. Chief among these is the ancient Malay culture, and the cultures of Malaysia's two most prominent trading partners throughout history--the Chinese, and the Indians. 
<p>These three groups are joined by a dizzying array of indigenous tribes, many of which live in the forests and coastal areas of Borneo. Although each of these cultures has vigorously maintained its traditions and community structures, they have also blended together to create contemporary Malaysia's uniquely diverse heritage.</p> 

<p>One example of the complexity with which Malaysia's immigrant populations have contributed to the nation's culture as a whole is the history of Chinese immigrants. The first Chinese to settle in the straits, primarily in and around Malacca, gradually adopted elements of Malaysian culture and intermarried with the Malaysian community. Known asbabas and nonyas, they eventually produced a synthetic set of practices, beliefs, and arts, combining Malay and Chinese traditions in such a way as to create a new culture.</p>
<p> Later Chinese, coming to exploit the tin and rubber booms, have preserved their culture much more meticulously. A city likePenang, for example, can often give one the impression of being in China rather than in Malaysia.</p> 
<p>Another example of Malaysia's extraordinary cultural exchange the Malay wedding ceremony, which incorporates elements of the Hindu traditions of southern India; the bride and groom dress in gorgeous brocades, sit in state, and feed each other yellow rice with hands painted with henna. 
<p>Muslims have adapted the Chinese custom of giving little red packets of money (ang pau) at festivals to their own needs; the packets given on Muslim holidays are green and have Arab writing on them.</p> 



