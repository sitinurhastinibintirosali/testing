        <p align="justify"><strong>        Places: </strong></p>
        
<p>Centuries ago merchant seamen following the trade winds discovered Malaysia. Captivated by its natural beauty and tropical bounty many returned again and again, some even choosing to make the idyll paradise of these shores home.</p>
<br><p>Today, Malaysia is a haven for adventure and nature lovers. Easy accessibility, the warm tropical climate, on-going conservation efforts and many beautiful nature spots all contribute to making Malaysia a premier eco-tourism destination.</p>
<br><p>Almost three-fifths of Malaysia's 330,433 sq. km land mass is covered by tropical rainforests which are home to a variety of exotic, rare and protected flora and fauna. Cascading waterfalls, rapid rivers, serene lakes, craggy mountains, secret caves, bountiful seas, kaleidoscopic coral reefs and scenic islands all offer something for each individual. Challenging jungle treks, leisurely swims, the adrenaline-pumping thrill of whitewater rafting or a quiet communion with nature are among the wide range of activities that can be enjoyed in Malaysia's fascinating outdoor environment.</p>
<br><p>The magical islands of Langkawi, Payar and Pangkor surrounded by the warm waters of the Straits of Malacca offer excellent diving opportunities all-year round. Off the eastern coast in the South China Sea, the paradise islands of Perhentian, Redang, Tenggol, Kapas, Tioman, Rawa, Sibu, Tinggi and Besar are reknowned for their marine diversity and clear waters. The dive sites off Sabah in Borneo are equally lovely; the most prominent being the islands of the Tuanku Abdul Rahman National Park, the Layang-Layang atoll and Pulau Mabul. For wreck diving, the island of Labuan, off Sabah's south-western coast, boasts of some of the region's most fabulous sites. Most of the dive sites in Malaysia are found within its marine parks which serve as sanctuaries for the protection and preservation of the fragile marine eco-systems with their wealth of marine life and vegetation.</p>
<br><p>Freshwater or inland fishing is an activity gaining in popularity. Tasik Kenyir, Tasek Bera, Tasik Chini and Tasik Dampar, and the Semborang Reservoir in Peninsular Malaysia are home to species like Belida, Toman, Grass Carp and Tinfoil Barb which await the avid angler's hooks.</p>
<br><p>The country's many rivers and streams also teem with many species of fresh-water fish.</p>
<br><p>The waters off the east coast and Sabah and Sarawak also beckon the deep sea or game fishing enthusiast. For those with a yen for speed, white water rafting in Sabah offers a truly exhilarating ride down swift rivers. Those seeking higher altitudes will enjoy the mountain climbing challenge of Gunung Kinabalu.</p>
<br><p>The more introspective visitor may prefer time alone with nature and activities such as bird watching, camping and cave exploring are satisfying ways to get to enjoy nature.</p>

 
 
