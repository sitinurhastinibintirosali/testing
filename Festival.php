        <p align="justify"><strong>        Festival: </strong></p>
        <p><strong>	Chinese New Year: </strong></p>
<p>Chinese New Year Is celebrated over a period of 15 days, beginning from the first day of the Chinese lunar calendar. It is a joyous occasion marked by family reunions, giving of red packets or 'ang pow' by parents to their children or among relatives and well wishers. This practice coupled with the giving away of oranges is intended as a symbol of prosperity and good luck for the recipients. Another feature is the traditional lion dance.</p>

<br><p><strong>Federal Territory Day:</strong></p>

<p>It was on this day in 1974 that KL was declared a Federal Territory. City dwellers observe the occasion with day-long competitions and performances at the city's main parks such as Lake Gardens and Taman Tasik Titiwangsa.<p>
<p><strong>Thaipusam:</strong></p>
<p>Thaipusam is a day for penance and atonement among the Hindu community. The festival begins with a grand procession in Kuala Lumpur of the silver chariot bearing the statue of Lord Subramaniam. A striking feature of the procession is the sight of thousands of coconuts being thrown on the streets and devotees carrying kavadis or wooden steel yokes with longspikes and metal hooks pierced into their bodies.</p>
<br><p><strong>Mega Sale Carnival:</strong></p>
<p>Malaysia is famous for its shopping where quality, variety and pricing is hard to beat. Mega Sales Carnival is held three times a year, in March, August and December and discounts galore are offered during these periods.</p>
<br><p><strong>Wesak Day:</strong></p>
<p>Wesak Day celebrated in May is the most auspicious day in the Buddhist calendar as it marks the birth, enlightenment and death of Buddha. Celebrations begin before dawn with Buddhist devotees gathering in temples throughout the country. It is a time for prayers, offerings, chanting and alms giving. A significant act at this time is the releasing of doves and tortoises at temples.</p>
<br><p><strong>Colours of Malaysia:</strong></p>
<p>The rich and intricate potpourri of cultures and traditions reflecting the proud and unique heritage of Malaysia is reflected in this event. This month long event filled with many activities at various venues in Kuala Lumpur is certainly not be missed.<p>
<p><strong>Food & Fruits Fiesta:</strong></p>
<p>Malaysia is endowed with a fabulous range of Asian and International cuisine. Coupled with a plethora of tropical, sub-tropical and even temperate fruits, Malaysia's Food and Fruit Festival offers a divine culinary experience that is difficult to find elsewhere.</p>
<br><p><strong>Merdeka Eve Celebration 2001</strong></p>- Kuala Lumpur
 <p>Join in the fun on the eve of the anniversary of Malaysia's national day. A carnival-like atmosphere pervades on this night of mesmerizing dances, dazzling colourful fireworks display and performances by local artistes, culminating in the Merdeka countdown for the raising of the jalur Gemilang at the midnight amidst patriotic Malaysian and tourists cheer the hearty shouts of 'Merdeka'.</p>
<br><p><strong>National Day:</strong></p>
<p>August 31 marks the nation's national day which is celebrated in Kuala Lumpur at the Dataran Merdeka or Merdeka Square situated in front of the Royal Selangor Club. Thousands of spectators converge on the city to watch the colourful parade along the streets of the city and performances held at the Merdeka Square. However, the celebrations are also rotated among other states.</p>
<br><p><strong>Deepavali:</strong></p>
<p>Among the Hindus, Deepavali or the Festival of Lights signifies the triumph of good over evil. Celebrated during the 7th month of the Hindu calendar, a traditional oil bath precedes the festivity. The celebration includes visits to temples and prayers at household altars. Hindu homes are adorned with lights or oil lamps to signify the victory of Lord Krishna over the demon Narakasura.</p>
<br><p><strong>Hari Raya Aidilfitri:</strong></p>
<p>A meaningful day of celebration for Muslims to mark the end of Ramadhan or the fasting month. Muslims usher in Hari Raya Aidilfitri with prayers in the mosque and asking forgiveness from family members. It is customary during this occasion for Muslims to open their homes to well-wishers as well as to visit friends and relatives. A special delicacy that is served at this time is Lemang, glutinous rice cooked in bamboo stems.</p>
<br><p><strong>Christmas.</strong></p>
<p>The festive air of Christmas is prevalent in the city especially in the decorations, caroling and partying during the occasion.
</p>
 


 
